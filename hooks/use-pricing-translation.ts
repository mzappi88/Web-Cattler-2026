export function usePricingTranslation(): {
  selectedCountry: Country;
  setSelectedCountry: (country: Country) => void;
  currency: string;
  t: (key: string) => any;
  formatPrice: (price: number) => string;
  isHydrated: boolean;
} {
  console.log("💰 Pricing Hook - Starting usePricingTranslation");
  
  // Use the main translation hook which already has admin mode logic
  const { selectedCountry, setSelectedCountry, isHydrated } = useTranslation();
  
  console.log("💰 Pricing Hook - Using translation hook:", { selectedCountry, isHydrated });
  
  // Debug: Check localStorage directly (with safe access for Safari incognito)
  if (typeof window !== 'undefined') {
    try {
      const savedCountry = localStorage.getItem("cattler-country");
      const adminMode = localStorage.getItem("cattler-admin-mode");
      console.log("💰 Pricing Hook - localStorage debug:", { savedCountry, adminMode });
      
      // Force override if in admin mode
      if (adminMode === "true" && savedCountry) {
        console.log("💰 Pricing Hook - Admin mode detected, forcing country to:", savedCountry);
        // Don't override here, let the main hook handle it
      }
    } catch (error) {
      console.warn("💰 Pricing Hook - Failed to access localStorage:", error);
    }
  }

  // Memoize currency to prevent unnecessary recalculations
  const currency = useMemo(() => countryCurrencyMap[selectedCountry], [selectedCountry])

  // Memoize current translations object - use country directly
  const currentTranslations = useMemo(() => {
    const translations = pricingTranslations[selectedCountry as keyof typeof pricingTranslations]
    if (!translations) {
      console.warn(`No translations found for country: ${selectedCountry}, falling back to US`)
      return pricingTranslations.US
    }
    return translations
  }, [selectedCountry])

  // Translation function that updates immediately when country changes
  const t = useCallback(
    (key: string): any => {
      const translation = (currentTranslations as any)?.[key]
      if (translation === undefined) {
        console.warn(`Translation missing for key: ${key} for country: ${selectedCountry}`)
        return key
      }
      return translation
    },
    [currentTranslations, selectedCountry],
  )

  // Format price with currency
  const formatPrice = useCallback(
    (price: number) => {
      if (price === undefined || price === null || isNaN(price)) {
        return "N/A";
      }
      return `${currency} ${price.toLocaleString()}`;
    },
    [currency],
  );

  return {
    selectedCountry,
    setSelectedCountry,
    currency,
    t,
    formatPrice,
    isHydrated,
  }
}

// Standalone formatPrice function for direct import
export function formatPrice(price: number, country: Country = "US") {
  const currency = countryCurrencyMap[country];
  if (price === undefined || price === null || isNaN(price)) {
    return "N/A";
  }
  return `${currency} ${price.toLocaleString()}`;
}
